/** Icone d'alerte selon le nom de la categorie d'urgence (fallback generique). */
export function iconForCategory(categoryName = "") {
  const name = categoryName.toLowerCase();
  if (name.includes("cardiaque") || name.includes("malaise")) return <HeartIcon />;
  if (name.includes("accident") || name.includes("route")) return <CarIcon />;
  if (name.includes("respir") || name.includes("asthme")) return <LungsIcon />;
  return <GenericAlertIcon />;
}
